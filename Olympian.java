package Main.Olympian;


public class Olympian {
    private String name;
    private Sex sex;
    private int age;

    public void setName(String name)
    {
        this.name = name;
    }

    public void setSex(Sex sex)
    {
        this.sex = sex;
    }

    public void setAge(int age)
    {
        this.age = age;
    }

    public String getname()
    {
        return this.name;
    }

    public Sex getSex()
    {
        return this.sex;
    }

    public int getAge()
    {
        return this.age;
    }
}
